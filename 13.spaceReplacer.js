function replaceSpaces(value) {
    value = value.replace(/ /g, '');
    jsConsole.writeLine(value);
}

replaceSpaces("We are living in a yellow submarine. We will move out of it in 5 days.");
replaceSpaces("No one heard a single word you said. They should have seen it in your eyes. What was going around your head.");
replaceSpaces("But you were living in another world tryin' to get your message through.");